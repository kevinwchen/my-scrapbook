// 'var' can be declared multiple times
// scope is global or local if within a function
var dogName = "Bobby";
var dogName = "Snowy";

// 'let' can only be declared once
// scope is limited to the block/statment/expression of declaration
let catName = "Quincy";
catName = "Beau";

// 'const' can only be declared once, is read-only
//  cannot be reassigned afterwards to prevent making mistakes later
const mouseName = "Jerry";
mouseName = "Remy";